namespace Procedural
{
    public struct Vector2F
    {
        public float x;
        public float y;

        public Vector2F(float x, float y)
        {
            this.x = x;
            this.y = y;
        }
    }

    public struct Vector2I
    {
        public int x;
        public int y;

        public Vector2I(int x, int y)
        {
            this.x = x;
            this.y = y;
        }
    }
}